#include <stdio.h>


// Conversão de volume em litros para metros cúbicos m3

int main(){
    
    float L = 55 ; // declarando variável "M", recebe valor de 55
    float M ; // declarando variável "L", 

    //imprimindo valor de "L"
    printf(" Exatos %f litros \n", L);

    M = L /1000 ; // Conversão de "L" litros  para metros cúbicos "M" 

    //imprimindo valor que "M" recebe
    printf("Conversao para metros cubicos : %f \n",M) ;

    return 0;

}
